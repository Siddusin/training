package techno.java.collectin;
class Friendship {
String name;
int age;
String Relation;
public Friendship(String name, int age, String relation) {
	super();
	this.name = name;
	this.age = age;
	Relation = relation;
}
@Override
public String toString() {
	return "\n"+"Name=" + name + "\n";
}


}
